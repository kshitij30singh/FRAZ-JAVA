public class typecasting {
    public static void main(String[] args)
    {
        int a = 10;
        long b = a;

        float c = 9.0f;
        double d = c;

       long x = 100000000000L;
       int y = (int)x;
       

       double z = 1.455555;
       float e = (float)z;

       int u = (int) z;
       System.out.println(u);

    }
}
